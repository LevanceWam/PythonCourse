course = " python programming "
print(course.upper())  # All Caps
print(course.lower())  # All Lowercase
print(course.title())  # Captialize Words
print(course.strip())  # Removes the white space from the beginning and end of a string\
print(course.find("pro"))  # Returns the index it is found
print(course.replace("p", "j"))  # Replaces the selected character
print("pro" in course)  # Returns a Boolean
# Used to see if we don't have a sequence of characters in a string
print("swift" not in course)
