course = "Python Programming"
# returns the amount of characters in a string
print(len(course))
# returns us the first letter of the string
print(course[0])
# last letter of the string
print(course[-1])
# first to the last letter of the string
print(course[0:3])
# from the first letter to the end of the string
print(course[0:])
# goes to the 3rd letter of the string
print(course[:3])
# prints the entire string
print(course[:])
